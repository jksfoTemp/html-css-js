		<h1>Michael Rescigno</h1>
        <p><strong>Chief Operations Officer</strong></p>
		<p>In the capacity of Chief Operations Officer, Michael Rescigno&#8217;s role is critical to City Park as he is ultimately responsible to implement the operational improvements and marketing opportunities for all our parking locations. Michael began his career in the parking industry in 1993 in New York City, where he managed over 40 properties at once, gaining experience in almost every parking application, including; hotels, merchant garages, residential garages, office and floor valets, restaurant valet, and multi-tenant office garages.</p>
		<p>Michael is also responsible for the development and professional growth of City Park&#8217;s entire field management team, which includes our Division, Area, and location managers throughout our company.</p>
		
