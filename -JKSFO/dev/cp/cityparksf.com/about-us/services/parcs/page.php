		<h1>PARCS</h1>
		<p>Our deep mastery of Parking Access Revenue Control Systems (PARCS) will give you and your business peace of mind &#8722; with 100% accountability and auditable revenue streams.</p>
<p>City Park provides industry-leading, revenue-control technology through our proven four-phase model:</p>
<ol><li>
<p><strong>Tech-savvy auditors </strong>&#8722; City Park employs the best people in the parking industry, and are constantly training, developing and supporting our staff to maintain the highest level of accountability.  We use internal and third-party monitoring systems to continually validate the integrity of all City Park staff &#8722; from attendants to management.</p>
</li>
<li>
<p><strong>Best Practice procedures </strong>&#8722; City Park employs the best practices of the parking industry at every step to validate entry and exit, control ingress and egress and provide up-to-the-minute notification of irregularities and exceptions. Controlling revenue is one of the most vital considerations in parking, and City Park&#8217;s approach is multifaceted and highly effective.</p>
</li>
<li>
<p><strong>Advanced technology </strong>&#8722; City Park invests and deploys the most advanced technology in the industry, all with the goal of increasing efficiency, control and profitability. We can recommend the optimal technology that best suits your operation, including solutions ranging from remotely monitored automated self-pay stations hotel valet parking solutions.</p>
</li>
<li>
<p><strong>Comprehensive reports </strong>&#8722; Generating timely, accurate and transparent reporting is critical to the success of our PARCS system. That&#8217;s why our staff, procedures and technology are all geared toward providing you with the critical reporting your operation needs to run efficiently and profitably.  With City Park reporting, nothing is left to doubt.  We keep accurate, daily accounting of all aspects of your parking operation.  Our comprehensive reports let you know everything is controlled, accounted for, and always ready for your review.</p>
</li>
</ol>
		
