		<h1>Services</h1>
		<p>As San Francisco&#8217;s foremost parking service provider, City Park combines boutique style business values with the latest technology, industry knowledge and management leadership. We understand the importance and value of high-quality parking operations for our clients. That is why in our client relationships, our focus is accountability and communication. As a City Park client, you will always know how your parking operation is performing, and you will always have access to our senior management team.</p>
<p>While achieving your financial goals is paramount, we also strive to improve your customers&#8217; overall parking experience. To do so, we regularly employ the following:</p>
<ul><li>
<p>Secret Shoppers &#8722; City Park undertakes anonymous site visits, evaluating areas such as garage and booth cleanliness, customer interactions, attendant attitude, uniform presentation and expedition of transactions.</p>
</li>
<li>
<p>Customer Comment Cards &#8722; We take your customers&#8217; written comments very seriously. Our managers are responsible for regularly tabulating and reviewing the results and finding suitable solutions and taking appropriate actions.</p>
</li>
<li>
<p>Monthly Physical Audits &#8722; These comprehensive onsite inspections cover all areas of operations and include a thorough checklist of safety measures and the implementation of physical upgrades.</p>
