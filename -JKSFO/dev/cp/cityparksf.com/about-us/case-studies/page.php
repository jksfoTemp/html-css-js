    <h1>Case Studies</h1>
    
    

    
        <h3>
            Lower Cost Structure - W Hotel San Francisco
        </h3>
        <p>Nestled in San Francisco&#8217;s SoMa District, the W Hotel is a 404-room Starwood property with a panoramic view of both the city skyline and the Bay Bridge. Because of no onsite inventory of parking spaces, high labor and insurance costs began to lower the revenue stream from the valet parking department. City Park implemented its &#8220;staff by demand&#8221; philosophy to discover efficiencies in the valet labor force and reduce the number of working hours, while still retaining a high service level. Paired with a lower cost structure in workers&#8217; compensation and liability insurance, City Park was able to contribute an increase of $200,000 to the bottom line of the hotel.</p>
    
        <h3>
            Marketing Success - Jessie Square Garage
        </h3>
        <p>Located near the southern Financial District in San Francisco, the Jessie Square Garage is a 377-space, self-park garage that serves local businesses and hotels. Opened in February of 2005, the garage faced many hurdles in attracting business &#8722; lack of visibility (underground), minimal opportunity for signage (construction zone) and accessibility (small alley). The garage was &#8220;in the red&#8221; for its first four months of operation. However, after conducting a marketing survey of the neighborhood and assessing revenue opportunities, City Park implemented a detailed marketing plan that included group discounting, a direct-mail piece and public outreach to local businesses. Within two months, the garage was financially profitable, and in the course of a year the impact of the marketing campaign contributed over $300,000 in new revenues to the garage.</p>
    
        <h3>
            Hotel Resources - Union Square Garage
        </h3>
        <p>The Union Square Garage is located in the heart of San Francisco&#8217;s most popular shopping destination. Surrounded by over six million square feet of retail space and 12,500 hotel rooms, the 985-space, self-park garage owned by the City and County of San Francisco serves nearly 900,000 parkers a year. Faced with an inventory of unused spaces in the garage, City Park negotiated the ability to park hotel guest cars from the nearby 685-room Grand Hyatt San Francisco in the unused spaces, and simultaneously was able to save the Grand Hyatt thousands of dollars a year in off-site parking costs. In return, the City and County of San Francisco realized an additional $600,000 in new revenues a year, with only a minimal of capital cost.</p>
    



